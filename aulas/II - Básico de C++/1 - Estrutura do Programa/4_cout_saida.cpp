# include <bits/stdc++.h>

using namespace std;

int main() {
    /* ---------------------------------------------------------*/
    /* ------------------------- cout ------------------------- */
    /* ---------------------------------------------------------*/

    /* o cout é uma forma de exibir uma mensagem, que aparece no terminal do usuário
    quando o programa é executado. Veja o exemplo: */
    cout << "cout é uma ferramenta que permite exibir mensagens no terminal\n\n";

    /* as mensagens podem incluir valores que estão no seu programa: */
    cout << "além de mensagens, podemos exibir valores. Por exemplo:\n";
    
    int quantidade = 3545;
    cout << "Eu gostaria de ter" << quantidade << " gatos, mas minha mãe não deixa\n\n";

    /* para indicar uma quebra de linha, ou seja, um "enter", basta usar \n */
    cout << "O caracter \\n indica uma quebra de linha.\n";
    cout << "linha 1\nlinha 2\nlinha 3\n";
}